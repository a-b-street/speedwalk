import type { Feature } from "geojson";
import { constructMatchExpression } from "svelte-utils/map";
import type { ExpressionSpecification } from "maplibre-gl";

/** Length in meters above which a crossing-tagged way is considered suspicious (may need splitting). */
export const CROSSING_LENGTH_WARNING_M = 20;

export interface NodeProps {
  id: number;
  tags?: Record<string, string>;
  is_crossing: boolean;
  is_explicit_crossing_no: boolean;
  is_generated_crossing?: boolean;
  is_manual_crossing?: boolean;
  modified: boolean;
  way_ids: number[];
  problems: Problem[];
}

export interface WayProps {
  id: number;
  tags: Record<string, string>;
  length_m?: number;
  kind:
    | "RoadWithSeparate"
    | "RoadWithTags"
    | "RoadWithoutSidewalksExplicit"
    | "RoadWithoutSidewalksImplicit"
    | "RoadUnknown"
    | "Sidewalk"
    | "Crossing"
    | "Other";
  modified: boolean;
  node_ids: number[];
  is_severance: boolean;
  is_service: boolean;
  is_manual_crossing?: boolean;
  problems: Problem[];
}

interface Problem {
  note: string;
  details: Feature[];
}

