/** Ordered kinds for roads and non-roads; shared by Sidewalks and Export legend/metrics. */
export const roadKinds = [
  "RoadWithSeparate",
  "RoadWithTags",
  "RoadWithoutSidewalksExplicit",
  "RoadWithoutSidewalksImplicit",
  "RoadUnknown",
] as const;
export const nonRoadKinds = ["Sidewalk", "Crossing", "Other"] as const;

type RoadKind = (typeof roadKinds)[number];
type NonRoadKind = (typeof nonRoadKinds)[number];
type LegendKind = RoadKind | NonRoadKind;

/** Legend items (label + color) for sidewalk/road kinds; same terms and colors on Export and Sidewalks. */
export const sidewalkLegendItems: { label: string; color: string }[] = [
  ...roadKinds,
  ...nonRoadKinds,
].map((kind: LegendKind) => ({
  label: kindLabels[kind],
  color: colors[kind],
}));

